import { fetchTodoList, getTodo, addTodo, updateTodo, delTodo } from "@/api/todo"
import Editor from '@/components/Editor';

export default {
  name: "Notice",
  components: {
    Editor
  },
  data() {
    return {
      // 遮罩层
      loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 公告表格数据
      noticeList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 类型数据字典
      statusOptions: [],
      // 状态数据字典
      typeOptions: [],
      // 责任人数据字典
      assignOptions: [],
      // 项目数据字典
      pjtOptions: [],
      // 所属平台产品数据字典
      productOptions: [],
      // 任务优先级
      priorityOptions: [],
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        noticeTitle: undefined,
        createBy: undefined,
        todoStatus: '0'
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        todoTitle: [
          { required: true, message: "任务名称不能为空", trigger: "blur" }
        ],
        todoType: [
          { required: true, message: "任务类型不能为空", trigger: "change" }
        ]
      }
    };
  },
  created() {
    this.getList();
    //任务状态
    this.getDicts("todo_status").then(response => {
      this.statusOptions = response.data;
    });
    //任务类型
    this.getDicts("todo_type").then(response => {
      this.typeOptions = response.data;
    });
    //优先级
    this.getDicts("todo_priority").then(response => {
      this.priorityOptions = response.data;
    });
    //所属平台产品类型
    this.getDicts("product_type").then(response => {
      this.productOptions = response.data;
    });
    //所属项目
    this.getDicts("project_name").then(response => {
      this.pjtOptions = response.data;
    });
    //责任人 TODO api
    this.assignOptions = [
      {dictLabel:'张三',dictValue:'1'},
      {dictLabel:'李四',dictValue:'2'},
    ]
  },
  methods: {
    /** 查询公告列表 */
    getList() {
      this.loading = true;
      fetchTodoList(this.queryParams).then(response => {
        this.noticeList = response.rows;
        this.total = response.total;
        this.loading = false;
      });
    },
    // 公告状态字典翻译
    statusFormat(row, column) {
      return this.selectDictLabel(this.statusOptions, row.todoStatus);
    },
    // 公告状态字典翻译
    typeFormat(row, column) {
      return this.selectDictLabel(this.typeOptions, row.noticeType);
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        noticeId: undefined,
        noticeTitle: undefined,
        noticeType: undefined,
        noticeContent: undefined,
        status: "0"
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.noticeId)
      this.single = selection.length!=1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.open = true;
      this.title = "添加公告";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const todoId = row.todoId || this.ids
      getTodo(todoId).then(response => {
        this.form = response.data;
        this.open = true;
        this.title = "修改任务";
      });
    },
    /** 提交按钮 */
    submitForm: function() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.todoId != undefined) {
            console.log(this.form)
            return false
            updateTodo(this.form).then(response => {
              this.msgSuccess("修改成功");
              this.open = false;
              this.getList();
            });
          } else {
            addTodo(this.form).then(response => {
              this.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const noticeIds = row.noticeId || this.ids
      this.$confirm('是否确认删除公告编号为"' + noticeIds + '"的数据项?', "警告", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning"
      }).then(function() {
        return delTodo(noticeIds);
      }).then(() => {
        this.getList();
        this.msgSuccess("删除成功");
      })
    }
  }
};
</script>
